n= int(input("enter a number (1-7):"))
if n == 1:
    print("SUNDAY")
elif n == 2:
    print("MONDAY")
elif n == 3:
    print("TUESDAY")
elif n == 4:
    print("WEDNESDAY")
elif n == 5:
    print("THURSDAY")
elif n == 6:
    print("FRIDAY")
elif n == 7:
    print("SATURDAY")
else:
    print("INVALID DAY")